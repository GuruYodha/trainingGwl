-- Create accounts table having id, name and balance field
-- Create account_audit table having account_id, old_balance, new_balance, changed_on fields

create table accounts(id SERIAL PRIMARY KEY,name VARCHAR(100),balance numeric);
create table account_audit(
account_id int,
old_balance numeric,
new_balance numeric,
changed_on timestamp DEFAULT CURRENT_TIMESTAMP
);

--Trigger Function
CREATE OR REPLACE FUNCTION log_balance_update()
RETURNS TRIGGER
AS $$
BEGIN
	INSERT INTO account_audit(account_id,old_balance,new_balance)
	VALUES (OLD.id,OLD.balance,NEW.balance);

	RETURN NEW;
END;
$$ LANGUAGE plpgsql;

------Trigger------
CREATE TRIGGER balance_update_trigger
AFTER UPDATE ON accounts
FOR EACH ROW
WHEN(OLD.balance IS DISTINCT FROM NEW.balance)
EXECUTE FUNCTION log_balance_update();


