select sum(price) as tot_rev,prod_name
from product inner join orders
on product.prod_id=orders.prod_id
group by orders.prod_id,prod_name;


--3. Products that have never been ordered

select prod_name,product.prod_id
from product left outer join orders
on product.prod_id=orders.prod_id
where orders.prod_id is NULL;

--4. Products with more repeat orders than new orders

select prod_name,orders.prod_id
from product inner join orders
on product.prod_id=orders.prod_id
group by orders.prod_id,prod_name
having count(*)>1

--5. Top 3 products based on number of orders

select orders.prod_id,prod_name,count(*)
from product inner join orders
on product.prod_id=orders.prod_id
group by orders.prod_id,prod_name
order by count(*) desc
limit 3;

