CREATE OR REPLACE FUNCTION  add_numbers(a INT,b INT)
RETURNS INT
AS $$
BEGIN
	RETURN a+b;
END;
$$ LANGUAGE plpgsql;

select add_numbers(100,50);

-- create a function that takes the product name as input and returns the price

CREATE OR REPLACE FUNCTION show_price (prd_name VARCHAR)
RETURNS INT
AS $$
DECLARE prod_price int;
BEGIN
	select price into prod_price
	from product where prod_name=prd_name;
	return prod_price;
END;
$$ LANGUAGE plpgsql;

select show_price('Meat')

-- create a function that takes column input and returns the table

CREATE OR REPLACE FUNCTION show_values(prd_name VARCHAR)
RETURNS TABLE(id INT, name VARCHAR(20))
AS $$
BEGIN
    RETURN QUERY 
    SELECT prod_id, prod_name
    FROM product
    WHERE prod_name = prd_name;
END;
$$ LANGUAGE plpgsql;

select show_values('Fish')

