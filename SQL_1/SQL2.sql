--write a procedure that takes prod id and discounted price as input and apply discounted price in all given prod ids

CREATE OR REPLACE PROCEDURE applyDiscount(prd_id INT,dis_price INT)
AS $$
BEGIN
	UPDATE product 
	SET price=dis_price
	WHERE prd_id=prod_id;
END;
$$ LANGUAGE plpgsql;

